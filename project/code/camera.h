#ifndef _CAMERA_H_
#define _CAMERA_H_
#include "zf_common_headfile.h"

/* ==================== 摄像头红外灯检测（CM7_1 视觉核）====================
 * 思路：车上红外灯板 + 机上摄像头镜头加红外滤光片(建议 车灯940nm + 镜头~920nm长通)，
 *       画面里基本只剩车灯一个亮点。本模块只做两件事：
 *         ① 阈值化(灯≈饱和，固定高阈值)；② 求亮点质心。
 *       质心相对图像中心的偏移 = "车相对飞机"的方向(像素)，经 IPC 发给飞控核 CM7_0。
 *
 * 抗信标(850nm@40kHz 红外 / 红光10Hz)干扰，三层：
 *   ① 主力(硬件)：滤光片把信标 850nm 挡在门外 → 画面里只剩车的940nm灯；
 *      (红光是可见光，红外滤光片本就挡掉；40kHz 对相机=常亮，分辨不了，所以靠波长分。)
 *   ② 软件 ROI 跟踪：锁定后只在"上一帧质心附近的小窗口"里找，
 *      信标即使闯入画面其它位置，在窗口外，自动被排除；
 *   ③ 面积门限：太小=噪点、太大=异常(整片亮/强反光)，都判丢目标。
 *
 * 不再用连通域/BFS/大数组 → 内存几乎为零、速度快(可关D-cache也够)、还更鲁棒。*/

#define CAM_ROWS   (120)   /* 图像高(行, y) */
#define CAM_COLS   (188)   /* 图像宽(列, x) */

/* ---- 可调参数(现场标定) ---- */
#define LED_THRESHOLD   (30)    /* 60→30：mg实测最高48，原阈值60检不出。降到30确保锁到灯板。注意背景噪声有无增加。 */
#define LED_MIN_AREA    (4)     /* 最小亮点数：< 此 判为噪点/无目标 */
#define LED_MAX_AREA    (6000)  /* 最大亮点数：> 此 判为异常(整片亮/强反光) */
#define ROI_HALF        (30)    /* 锁定后跟踪窗口半边长(像素)：信标在窗口外会被自动排除 */

/* ---- 输出 ---- */
extern float cam_error_x;   /* 质心相对图像中心 水平偏移(像素), += 右 */
extern float cam_error_y;   /* 质心相对图像中心 垂直偏移(像素), += 下 */
extern int   cam_area;      /* 本帧亮点数(近似连通域大小) */
extern uint8 cam_locked;    /* 1=锁到车灯, 0=没锁到 */
extern uint8 cam_max_gray;  /* 诊断：本帧扫描区最大灰度(未锁=全图)。判灯多亮/阈值设多少：maxg远高于阈值=正常 */

/* 处理一帧(在 mt9v03x_finish_flag 置位后调)。返回 cam_locked。 */
uint8 camera_process(void);

#endif
